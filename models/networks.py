# Unet parts
class DoubleConv(nn.Module):
    """(convolution => [BN] => ReLU) * 2"""

    def __init__(self, in_channels, out_channels, mid_channels=None):
        super().__init__()
        if not mid_channels:
            mid_channels = out_channels
        self.double_conv = nn.Sequential(
            nn.Conv2d(in_channels, mid_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(mid_channels),
            nn.ReLU(inplace=True),
            nn.Conv2d(mid_channels, out_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True)
        )

    def forward(self, x):
        return self.double_conv(x)


class Down(nn.Module):
    """Downscaling with maxpool then double conv"""

    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.maxpool_conv = nn.Sequential(
            nn.MaxPool2d(2),
            DoubleConv(in_channels, out_channels)
        )

    def forward(self, x):
        return self.maxpool_conv(x)


class Up(nn.Module):
    """Upscaling then double conv"""

    def __init__(self, in_channels, out_channels, bilinear=True):
        super().__init__()

        # if bilinear, use the normal convolutions to reduce the number of channels
        if bilinear:
            self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
            self.conv = DoubleConv(in_channels, out_channels, in_channels // 2)
        else:
            self.up = nn.ConvTranspose2d(in_channels, in_channels // 2, kernel_size=2, stride=2)
            self.conv = DoubleConv(in_channels, out_channels)

    def forward(self, x1, x2):
        x1 = self.up(x1)
        # input is CHW
        diffY = x2.size()[2] - x1.size()[2]
        diffX = x2.size()[3] - x1.size()[3]

        x1 = F.pad(x1, [diffX // 2, diffX - diffX // 2,
                        diffY // 2, diffY - diffY // 2])
        # if you have padding issues, see
        # https://github.com/HaiyongJiang/U-Net-Pytorch-Unstructured-Buggy/commit/0e854509c2cea854e247a9c615f175f76fbb2e3a
        # https://github.com/xiaopeng-liao/Pytorch-UNet/commit/8ebac70e633bac59fc22bb5195e513d5832fb3bd
        x = torch.cat([x2, x1], dim=1)
        return self.conv(x)


class OutConv(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(OutConv, self).__init__()
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=1)

    def forward(self, x):
        return self.conv(x)


class UNet(nn.Module):
    def __init__(self, n_channels=11, n_classes=3, bilinear=True):
        super(UNet, self).__init__()
        self.n_channels = n_channels
        self.n_classes = n_classes
        self.bilinear = bilinear

        self.inc = DoubleConv(n_channels, 64)
        self.down1 = Down(64, 128)
        self.down2 = Down(128, 256)
        self.down3 = Down(256, 512)
        self.down4 = Down(512, 512)
        self.up1 = Up(1024, 256)
        self.up2 = Up(512, 128)
        self.up3 = Up(256, 64)
        self.up4 = Up(128, 64)
        self.outc = OutConv(64, n_classes)

    def forward(self, x):
        # x = torch.nn.Sigmoid()(x)
        x1 = self.inc(x)
        x2 = self.down1(x1)
        x3 = self.down2(x2)
        x4 = self.down3(x3)
        x5 = self.down4(x4)
        x = self.up1(x5, x4)
        x = self.up2(x, x3)
        x = self.up3(x, x2)
        x = self.up4(x, x1)
        x = self.outc(x)
        x = torch.nn.Tanh()(x)
        # print(x.shape)
        return x


class SGUIENet(nn.Module):
    def __init__(self, in_channels, out_channels, dim, kernel_size=3, blocks=4, groups=3):
        super(SGUIENet, self).__init__()
        self.conv1 = conv(in_channels, dim, kernel_size)
        self.fab = FABlock(dim, kernel_size)
        self.orb = nn.Sequential(*[Group(dim, kernel_size, blocks) for _ in range(groups)])
        self.fa = FA()
        self.ff_conv1 = conv(dim, dim, kernel_size)
        self.ff_conv2 = conv(dim, dim, kernel_size)
        self.ff_conv3 = DoubleConv(dim, dim)
        self.sm = nn.Sigmoid()
        self.conv2 = conv(dim, out_channels, kernel_size)

    def forward(self, raw, regions, region_masks, region_idxs):
        # main branch
        x1 = self.conv1(raw)
        x1 = self.fab(x1)
        # feature attention
        region_imgs = []
        for region, region_mask, region_idx in zip(regions, region_masks, region_idxs):
            (x, y, w, h) = region_idx
            # print(region_idx)
            res, region_img = self.fa(region)
            region_imgs.append(region_img)
            # feature fusion
            res = self.ff_conv3(res)
            res = self.sm(res)
            res = res * region_mask
            tmp = x1[:, :, y:y + h, x:x + w].clone() * res
            x1[:, :, y:y + h, x:x + w] = x1[:, :, y:y + h, x:x + w] + tmp
            #x1[:, :, y:y + h, x:x + w] = x1[:, :, y:y + h, x:x + w] * res

        x1 = self.orb(x1)
        x1 = self.conv2(x1)
        x1 += raw
        x1 = torch.tanh(x1)

        return x1, region_imgs
