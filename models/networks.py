def conv(in_channels, out_channels, kernel_size, bias=False, stride=1):
    return nn.Conv2d(
        in_channels, out_channels, kernel_size,
        padding=(kernel_size // 2), bias=bias, stride=stride)


## Channel Attention Layer
class CALayer(nn.Module):
    def __init__(self, channel, reduction=8, bias=True):
        super(CALayer, self).__init__()
        # global average pooling: feature --> point
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        # feature channel downscale and upscale --> channel weight
        self.conv_du = nn.Sequential(
            nn.Conv2d(channel, channel // reduction, 1, padding=0, bias=bias),
            nn.ReLU(inplace=True),
            nn.Conv2d(channel // reduction, channel, 1, padding=0, bias=bias),
            nn.Sigmoid()
        )

    def forward(self, x):
        y = self.avg_pool(x)
        y = self.conv_du(y)
        return x * y


## Pixel Attention Layer
class PALayer(nn.Module):
    def __init__(self, channel, reduction=8, bias=True):
        super(PALayer, self).__init__()
        self.pa = nn.Sequential(
            nn.Conv2d(channel, channel // reduction, 1, padding=0, bias=bias),
            nn.ReLU(inplace=True),
            nn.Conv2d(channel // reduction, 1, 1, padding=0, bias=bias),
            nn.Sigmoid()
        )

    def forward(self, x):
        y = self.pa(x)
        return x * y


## Features Attention include Channel attention and Pixel Attention
class FABlock(nn.Module):
    def __init__(self, dim, kernel_size, ):
        super(FABlock, self).__init__()
        self.conv1 = conv(dim, dim, kernel_size, bias=True)
        self.act1 = nn.ReLU(inplace=True)
        self.conv2 = conv(dim, dim, kernel_size, bias=True)
        self.calayer = CALayer(dim)
        self.palayer = PALayer(dim)

    def forward(self, x):
        res = self.act1(self.conv1(x))
        res = res + x
        res = self.conv2(res)
        res = self.calayer(res)
        res = self.palayer(res)
        res = res + x
        return res


## Channel Attention Block(CAB)
class CAB(nn.Module):
    def __init__(self, n_feat, kernel_size, reduction, bias, act):
        super(CAB, self).__init__()
        modules_body = []
        modules_body.append(conv(n_feat, n_feat, kernel_size, bias=bias))
        modules_body.append(act)
        modules_body.append(conv(n_feat, n_feat, kernel_size, bias=bias))

        self.CA = CALayer(n_feat, reduction, bias=bias)
        self.body = nn.Sequential(*modules_body)

    def forward(self, x):
        res = self.body(x)
        res = self.CA(res)
        res = res + x
        return res


# Attention augmented feature
class FA(nn.Module):
    def __init__(self, in_channels=3, out_channels=3, dim=64, kernel_size=3):
        super(FA, self).__init__()
        self.conv1 = conv(in_channels, dim, kernel_size)
        self.fab = FABlock(dim, kernel_size)
        self.unet = UNet(dim, dim)
        self.conv2 = conv(dim, out_channels, kernel_size)

    def forward(self, raw):
        x = self.conv1(raw)
        x1 = self.fab(x)
        x2 = self.unet(x1)
        x3 = self.conv2(x2.clone())
        img = x3 + raw
        return x2, img


class Group(nn.Module):
    def __init__(self, dim, kernel_size, blocks):
        super(Group, self).__init__()
        modules = [FABlock(dim, kernel_size) for _ in range(blocks)]
        modules.append(conv(dim, dim, kernel_size))
        self.gp = nn.Sequential(*modules)

    def forward(self, x):
        res = self.gp(x)
        res = res + x
        return res


